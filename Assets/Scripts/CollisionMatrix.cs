    public void setMatrix()
    {
        if (currentMatrix == 1)
        {
            matrix = matrix_outside;
            currentMatrix = 0;
        }
        else
        {
            matrix = matrix_inside;
            currentMatrix = 1;
        }
    }

    public void addCollision(int x, int y)
    {
        matrix[y, x] = 1;
    }

    public void addCollision(Vector2Int pos)
    {
        matrix[pos.y, pos.x] = 1;
    }

    public int getCollisionType(int x, int y)
    {
        int width = matrix.GetLength(1);
        int height = matrix.GetLength(0);

        if (x < 0 || x > width || y < 0 || y > height)
            return 1;

        return matrix[y, x];
    }
